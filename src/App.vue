            <p class="text-muted">The Boston terrier breed originated around 1875, when Robert C. Hopper of Boston, purchased from Edward Burnett a dog named Judge (known later as Hooper's Judge), which was of a Bull and Terrier type lineage. Hooper's Judge is directly related to the original Bull and Terrier breeds of the 19th and early 20th centuries. The American Kennel Club cites Hooper's Judge as the ancestor of all true modern Boston Terriers.[9]
Judge weighed about 32 pounds (15 kg). Judge was bred to Burnett's Gyp (or Kate).  Gyp was a white bulldog-type female, owned by Mr. Edward Burnett, of Southboro, MA. She weighed about 20 pounds, was stocky and strong and had the typical blocky head now shown in Bostons. From this foundation of the breed, subsequent breeders refined the breed into what we know of it today. [10] Bred down in size from fighting dogs of the Bull and Terrier types, the Boston Terrier originally weighed up to 44 pounds (20 kg) (Old Boston Bulldogs).[2] The breed was first shown in Boston in 1870. By 1889 the breed had become sufficiently popular in Boston that fanciers formed the American Bull Terrier Club, the breed's nickname, "roundheads". Shortly after, at the suggestion of James Watson (a noted writer and authority), the club changed its name to the Boston Terrier Club and in 1893 it was admitted to membership in the American Kennel Club, thus making it the first US breed to be recognized.[9] It is one of a small number of breeds to have originated in the United States. The Boston Terrier was the first non-sporting dog breed in the US.
In the early years, the color and markings were not very important. By the 20th century the breed's distinctive markings and color were written into the standard, becoming an essential feature. The Boston Terrier has lost most of its aggressive nature, preferring the company of humans, although some males will still challenge other dogs if they feel their territory is being invaded. Boston University's mascot is Rhett the Boston Terrier. The Boston Terrier is also the mascot of Wofford College in Spartanburg, S.C..</p>
